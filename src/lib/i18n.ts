export type Dict = Record<keyof (typeof DICT)["ms"], string>;

export function getDict(lang: Lang): Dict {
  return DICT[lang];
}

/** Resolve the portal language from the `lang` cookie (default BM). */
export async function getLang(): Promise<Lang> {
  const c = (await cookies()).get("lang")?.value;
  return c === "en" ? "en" : "ms";
}
